#Creating class, methods and attributes


class People():

    def __init__(self, name, age, weight, height):
        self.name = name
        self.age = age
        self.weight = weight
        self.height = height

    def weightUp(self, weight):
        self.weight += weight

    def heighter(self, height):
        self.height += height

    




